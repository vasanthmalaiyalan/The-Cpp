#include <iostream>
#include <string>

int main() {
    
    // Integer variable
    int age = 25;
    
    // Floating point variable
    double height = 5.9;

    // Character variable
    char grade = 'A';

    // String variable
    std::string name = "Vasanth";
    
    // Boolean Variable
    bool is_student = true;

    // Output all vairable
    std::cout << "Name: " << name << "\n";
    std::cout << "Age: "  << age  << "\n";
    std::cout << "Height: " << height << "\n";
    std::cout << "Grade: " << grade << "\n";
    std::cout << "Student: " << is_student << "\n";
    
    return 0;
}